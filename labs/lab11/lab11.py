class IteratorRestart:
    """
    >>> iterator = IteratorRestart(2, 7)
    >>> for num in iterator:
    ...     print(num)
    2
    3
    4
    5
    6
    7
    >>> for num in iterator:
    ...     print(num)
    2
    3
    4
    5
    6
    7
    """
    def __init__(self, start, end):
        self.start = start - 1
        self.end = end
        self.iter = self.start

    def __next__(self):
        if self.iter >= self.end:
            self.iter = self.start
            raise StopIteration
        self.iter += 1
        return self.iter

    def __iter__(self):
        return self


