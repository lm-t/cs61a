class Str:
    """
    >>> s = Str("hello")
    >>> for char in s:
    ...     print(char)
    ...
    h
    e
    l
    l
    o
    >>> for char in s:    # a standard iterator does not restart
    ...     print(char)
    """
    def __init__(self, string):
        self.string = string

    def __next__(self):
        if self.string == '':
            raise StopIteration
        popped = self.string[0]
        self.string = self.string[1:]
        return popped

    def __iter__(self):
        return self


